# Completionist Checklist

'''wip'''

* Complete the Pokedex
* Collect all 14 badges
* Collect all TM and HM 
* Open the Slowpoke Well B2F chamber
* Save the Route KaJo schoolgirl
* Beat the generals on hardmode after you reach the credits once
* Solve all five of the Kimono Cabin puzzles
* Solve all the Ruins of Alph puzzles
* Solve the Violet Catacombs puzzle
* Reach the bottom of the Enigma Glacier
* Reach B2F of Union Cave 
* Help the old man in Gauldenrod
* Read your Rival's letter to his father 
* Return the wayward Nomad in the Vortex Caverns 
* Complete the Master Ball trade quest
* Find all four hidden grottoes
